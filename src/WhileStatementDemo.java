/*This program is for looping statement
 * We are using while statement
 * This will execute the while block till the condition satisfied
 */


public class WhileStatementDemo {
	
	public static void main(String[] args) {
		int count = 10;  
		
		while(count <= 20) 
		{
			System.out.println("Running in loop " + count);
			count++;
		}
		
	}

}
